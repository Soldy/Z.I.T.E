
function c(s){
    return document.getElementsByClassName(s)[0];
}
function cs(s){
    return document.getElementsByClassName(s)[0].style;
}

function cv(s){
    return document.getElementsByClassName(s)[0].value;
}

function cw(s, v){
    return document.getElementsByClassName(s)[0].value=v;
}


function d(s){
    return document.getElementById(s);
}
function ds(s){
    return document.getElementById(s).style;
}

function dv(s){
    return document.getElementById(s).value;
}

function dw(s, v){
    return document.getElementById(s).value = v;
}


